#include<stdio.h>
void main()
{

	int pos,num,i,j;
	printf("Enter a number\n");
	scanf("%d",&num);

	////////////////////////////////////////////////////////////////////////
	printf("Before Reverse:%d\n",num);
	for(pos=31;pos>=0;pos--)
		printf("%d",num>>pos&1);
	printf("\n");
	///////////////////////////////////////////////////////////////////////

	for(i=0,j=31;i<j;i++,j--)
	{
		if(num>>i&1!=num>>j&1)
		{
			num ^= 1<<i;
			num ^= 1<<j;
		}

	}
	////////////////////////////////////////////////////////////////////////
	printf("After Reverse:%d\n",num);
	for(pos=31;pos>=0;pos--)
		printf("%d",num>>pos&1);
	printf("\n");
	///////////////////////////////////////////////////////////////////////

}
