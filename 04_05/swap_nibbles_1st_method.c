#include<stdio.h>
void main()
{
int num,pos,i,j;
printf("Enter the number\n");
scanf("%d",&num);

////////////////////////////////////////////////////////////////////////
printf("Before Nibble Swap:%d\n",num);
for(pos=7;pos>=0;pos--)
printf("%d",num>>pos&1);
printf("\n");
///////////////////////////////////////////////////////////////////////

for(i=0,j=4;i<4;i++,j++)
{
if(num>>i&1!=num>>j&1)
{
num ^=  1<<i;
num ^=  1<<j;
}	
}


////////////////////////////////////////////////////////////////////////
printf("After Nibble Swap:%d\n",num);
for(pos=7;pos>=0;pos--)
printf("%d",num>>pos&1);
printf("\n");
///////////////////////////////////////////////////////////////////////







}
